
public class Reservacion {
	public String nombre;
	public int noPersonas;
	public int tiempoMilitar;
	public int minutosActiva;

	public Reservacion(String nombre, int noPersonas, int tiempoMilitar, int minutosActiva) {
		this.nombre = nombre;
		this.noPersonas = noPersonas;
		this.tiempoMilitar = tiempoMilitar;
		this.minutosActiva = minutosActiva;
	}

}
